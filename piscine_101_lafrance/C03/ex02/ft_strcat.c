char	*ft_strcat(char *dest, char *src)
{
	int i;
	int o;

	i = 0;
	o = 0;
	while (dest[o] != '\0')
	{
		o++;
	}
	while (src[i] != '\0')
	{
		dest[o] = src[i];
		o++;
		i++;
	}
	dest[o] = '\0';
	return (dest);
}
